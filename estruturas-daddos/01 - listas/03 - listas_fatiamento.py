# além de acessar, podemos extrair um conjunto de valores de uma sequência
letras = list('python')

print(letras[2:])


print(letras[:2])

# extrai apenas as posições 1 e 2 da lista
print(letras[1:3])

# extrai apenas a posição inicial e a 2
print(letras[0:3:2])


print(letras[::])

# ordem inversa da lista
print(letras[::-1])